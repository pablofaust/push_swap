#include "libft.h"

int		ft_strequ(const char *s1, const char *s2)
{
	unsigned int	i;

	if (s1 == 0 || s2 == 0)
		return (0);
	i = 0;
	if (ft_strlen(s1) != ft_strlen(s2))
		return (0);
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			return (0);
		i++;
	}
	return (1);
}
