#include "libft.h"

int		ft_tab_is_sort(int *tab, int nb)
{
	int	i;

	i = 0;
	while (i + 1 < nb)
	{
		if (tab[i + 1] > tab[i])
			return (0);
		i++;
	}
	return (1);
}
